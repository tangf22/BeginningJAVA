public class DrawFigures {
    public static void main (String [] args)
    {
        top(); // call
        low(); // method
        top ();
        low ();
    }
    public static void top()
    {
        System.out.println("   /\\");
        System.out.println("  /  \\");
        System.out.println(" /    \\");

    }
    public static void low()
    {
        System.out.println(" \\    /");
        System.out.println("  \\  /");
        System.out.println("   \\/");
    }

}
